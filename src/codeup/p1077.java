package codeup;

import java.util.Scanner;

public class p1077 {
    public static void main(String[] args) {
        int inputNum;

        Scanner scanner = new Scanner(System.in);
        inputNum = scanner.nextInt();

        for (int i = 0; i <= inputNum; i++) {
            System.out.printf("%d\n", i);
        }
    }
}
